#int () : construye un número entero a partir de un literal entero, un literal flotante(redondeando hacia abajo al número entero anterior)
#o un literal de cadena (siempre que la cadena represente un número entero)
#float () : construye un número flotante a partir de un literal entero, un literal flotante o un literal de cadena (siempre que la cadena
#represente un número flotante o entero)
#str () : construye una cadena a partir de una amplia variedad de tipos de datos, incluidas cadenas, literales enteros y literales flotantes

#ENTEROS

x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3

print(x)
print(x)
print(x)
#FLOTADORES

x = float(1)     # x will be 1.0
y = float(2.8)   # y will be 2.8
z = float("3")   # z will be 3.0
w = float("4.2") # w will be 4.2

print(x)
print(y)
print(z)
print(w)

#INSTRUMENTOS DE CUERDA

x = str("s1") # x will be 's1'
y = str(2)    # y will be '2'
z = str(3.0)  # z will be '3.0'

print(x)
print(y)
print(z)
